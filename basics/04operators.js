// ## using short hand
// +=,-=,*=,/=,%=,++


// ### ++ 
// this operator can be used before and after the variable
// ++age // age will be increased before this line is executed
// age++ // age will be increased after this line is executed

let age = 90;
console.log(++age) // 91
console.log(age) // 91


let marks = 90;
console.log(marks++) // 90
console.log(marks) // 91


